package org.candlepin.liquibase.uuid;

/**
 * POJO to represent a foreign key constraint that we have to drop/re-enable
 */
public class ForeignKeyConstraint {
    String name;
    String definition;

    public ForeignKeyConstraint(String name, String definition) {
        this.name = name;
        this.definition = definition;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDefinition() {
        return definition;
    }

    public void setDefinition(String definition) {
        this.definition = definition;
    }

    @Override
    public String toString() {
        return "ForeignKeyConstraint(\"" + name + "\"): " + definition;
    }
}
